const mutations = {
  SET_LATEST_TIMESTAMP(state, timestamp) {
    state.latestTimestamp = timestamp;
  },
  SET_SENSOR_DATA(state, data) {
    state.sensorData = data;
  },
  SET_TOP_SPEED_RANKS(state, ranks) {
    state.topSpeedRanks = ranks;
  },
  SET_STREET_AVERAGES(state, averages) {
    state.streetAverages = averages;
  },
  SET_OVERALL_AVERAGE_SPEED(state, speed) {
    state.overallAverageSpeed = speed;
  },
  INIT_CURRENT_SPEEDS(state) {
    state.currentSpeeds = state.initialMarkers.reduce((acc, marker) => {
      acc[marker.sensor_id] = 0;
      return acc;
    }, {});
  },
  UPDATE_CURRENT_SPEED(state, { sensor_id, speed }) {
    state.currentSpeeds[sensor_id] = speed;
  }
}

const actions = {
  updateLatestTimestamp({ commit }, timestamp) {
    commit('SET_LATEST_TIMESTAMP', timestamp);
  },
  updateSensorData({ commit }, data) {
    commit('SET_SENSOR_DATA', data);
  },
  updateTopSpeedRanks({ commit }, ranks) {
    commit('SET_TOP_SPEED_RANKS', ranks);
  },
  updateStreetAverages(streetAverages) {
    commit('SET_STREET_AVERAGES', streetAverages);
  },
  updateOverallAverageSpeed({ commit }, speed) {
    commit('SET_OVERALL_AVERAGE_SPEED', speed);
  },
  initCurrentSpeeds({ commit }) {
    commit('INIT_CURRENT_SPEEDS');
  },
  updateCurrentSpeed({ commit }, { sensor_id, speed }) {
    commit('UPDATE_CURRENT_SPEED', { sensor_id, speed });
  }
}

const getters = {
  latestTimestamp: state => state.latestTimestamp,
  sensorData: state => state.sensorData,
  topSpeedRanks: state => state.topSpeedRanks,
  streetAverages: state => state.streetAverages,
  overallAverageSpeed: state => state.overallAverageSpeed,
  currentSpeeds: state => state.currentSpeeds,
  initialMarkers: state => state.initialMarkers
}

export default {
  namespaced: true,
  state,
  mutations,
  actions,
  getters
}
